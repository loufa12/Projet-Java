
public class Score {
	protected int id_score;
	protected int score;

	public Score(int id_score, int score) {
		this.id_score = id_score;
	}

	public int getId_score() {
		return id_score;
	}

	public void setId_score(int id_score) {
		this.id_score = id_score;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}
}
